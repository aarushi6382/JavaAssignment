package Encapsulation_Inheritance;

public class Member {
    String name;
    int age;
    long phone_no; 
    String address;
    double salary;

    void printSalary() {
        System.out.println("Name = " + name);
        System.out.println("Age = " + age);
        System.out.println("Phone_No = " + phone_no);
        System.out.println("Address = " + address);
        System.out.println("Salary = " + salary);
    }
}
